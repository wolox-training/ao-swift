import UIKit

extension UIColor {
    static func backgroundPolar() -> UIColor {
        return UIColor(red: 0.92, green: 0.96, blue: 0.98, alpha: 1)
    }
    static func cerulean() -> UIColor {
        return UIColor(red: 0.00, green: 0.68, blue: 0.93, alpha: 1)
    }
    static func ceruleanFirstGradient() -> UIColor {
        return UIColor(red: 0.0, green: 0.68, blue: 0.93, alpha: 1)
    }
    static func ceruleanSecondGradient() -> UIColor {
        return UIColor(red: 0.22, green: 0.8, blue: 0.8, alpha: 1)
    }
    static func monza() -> UIColor {
        return UIColor(red: 0.82, green: 0.01, blue: 0.11, alpha: 1)
    }
    static func grey() -> UIColor {
        return UIColor(red: 0.79, green: 0.79, blue: 0.79, alpha: 1)
    }
    static func greySecondGradient() -> UIColor {
        return UIColor(red: 0.87, green: 0.87, blue: 0.87, alpha: 1)
    }
    static func greyThirdGradient() -> UIColor {
        return UIColor(red: 0.89, green: 0.89, blue: 0.89, alpha: 1)
        
    }
}
